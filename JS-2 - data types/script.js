// In js every value is either a primitive or an object/JS has dynamic typing
// * data types

// Number
let age = 32;
console.log(typeof age);

//String
let quote = "Hello there";
console.log(typeof quote);

//Boolean
let isAdult = true;
console.log(typeof isAdult);

// undefined
let kidsName = undefined;
console.log(typeof kidsName);

//null
let emptyValue = null;
console.log(typeof emptyValue);

// Symbol ES2015
let hello = Symbol("Hello");
console.log(typeof hello);

//Big int ES2020 very large integer that allocates a very large number...
let bigNumber = 123132135564444564654654654;
console.log(typeof bigNumber);
